Calculator = {
  add: function(a, b){
    console.log(a + b);
  },
  minus: function (a, b){
    console.log(a - b);
  },
  times: function(a, b){
    console.log(a * b);
  },
  devide: function(a, b){
    console.log(a / b);
  },
  powerof: function(a, b){
    console.log(a ** b);
  },
}

Calculator.add(3, 4);
Calculator.minus(3, 4);
Calculator.times(3, 4);
Calculator.devide(3, 4);
Calculator.powerof(3, 4);